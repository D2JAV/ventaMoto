package app.clases;

/**
 *
 * @author ASUS
 */
public class Detalle {
    String propiedad;
    String valor;

    public Detalle() {
    }

    public Detalle(String propiedad, String valor) {
        this.propiedad = propiedad;
        this.valor = valor;
    }

    public String getPropiedad() {
        return propiedad;
    }

    public void setPropiedad(String propiedad) {
        this.propiedad = propiedad;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }
    
    
    
}
